/* Faça um programa que compare um número com 10 e imprime na tela "o número é maior, 
o número é menor o número é igual" */

const number = 12

if (number < 10) {
    console.log("Este número é menor que 10")    
}
else if (number === 10) {
    console.log("Este número é igual a 10")
}
else{
    console.log("Este número é maior que 10")
}