/* OPERADORES ARITIMÉTICOS */

const n1 = 20
const n2 = 30
const n3 = 3
const n6 = 2

// Adição:

console.log(n1 + n2)

// Subtração:

console.log(n1 - n2)

// Multiplicação:

console.log(n2 * n3)

// Divisão:

console.log(n2 / n3)

// Resto:

console.log(n2 % n3)

// Incremento:
let n4 = 10

console.log(++n4) //11
n4++ //12
n4++ //13
n4++ //14

console.log(n4)

// Decremento:

let n5 = 40

console.log(--n5) //39
n5-- //38
n5-- //37
n5-- //36

console.log(n5)

// Exponencial:

console.log(n3 ** n6)